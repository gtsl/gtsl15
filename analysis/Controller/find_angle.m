function angle = find_angle(area)

% maps area of pins (in2) to commanded angle (deg)
% If area is too large, default to max angle

% From measurements in solidworks
% [ Angle (deg), Single area (in2), Total area (in2) ]
baseTable = ...
[0	0	0;...
10	0.0093	0.0279;...
15	0.0499	0.1497;...
20	0.0992	0.2976;...
25	0.1524	0.4572;...
30	0.209	0.627;...
35	0.2672	0.8016;...
40	0.3229	0.9687;...
45	0.3734	1.1202;...
50	0.417	1.251;...
55	0.452	1.356;...
60	0.4792	1.4376;...
65	0.5005	1.5015;...
70	0.5171	1.5513;...
75	0.5299	1.5897;...
80	0.5396	1.6188;...
85	0.5466	1.6398;...
90	0.5512	1.6536];

totalArea = baseTable(:,3);
angleData = baseTable(:,1);

if area < 0
    angle = 0;
elseif totalArea < area
    angle = 90; % degree
else
    angle = interp1(totalArea,angleData,area);
    if angle < 0
        angle = 0;
    end
end

end